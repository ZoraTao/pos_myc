<template>
  <div class="integral-det-box">
    <el-table
      :data="pointsList"
      stripe
      size="small"
      align="left"
      style="width: 100%;margin-bottom: 10px;">
      <el-table-column
        label="奖励积分"
        width="100px">
        <template slot-scope="scope">
          <p v-if="scope.row.pointAmount < 0 " class="am-ft-blue">{{scope.row.pointAmount}}</p>
          <p v-else class="am-ft-orange">+{{scope.row.pointAmount}}</p>
        </template>
      </el-table-column>
      <el-table-column
        prop="type"
        label="奖励类型"
        width="100px">
      </el-table-column>
      <el-table-column
        prop="createTime"
        label="奖励时间">
      </el-table-column>
    </el-table>
    <!--分页-->
    <el-pagination
      background
      class="am-ft-right mgt10"
      layout=" prev, pager, next"
      :page-size="5"
      :total="count"
      @current-change="handleCurrentChange"
      :current-page.sync="nub">
    </el-pagination>
  </div>
</template>

<script>
  export default {
    name: "member-integral-details",
    data() {
      return {
        pointsList: [], //积分明细列表
        count: 0,
        nub: 1,
        size: 10
      }
    },
    props:['memberInfo'],
    created: function () {
      this.getMemberPoints()
    },
    methods: {
      //查询积分明细
      getMemberPoints(){
        var _this = this;
        _this.$axios({
          url: 'http://myc.qineasy.cn/points-api/pointsFlow/getMemberPointLow',
          method: 'post',
          params: {
            jsonObject: {
              memberCardId: _this.memberInfo.memberCardId,
              memberId: _this.memberInfo.memberId,
              nub: (_this.nub==0?0:(_this.nub-1)*_this.size),
              size: _this.size
            },
            keyParams: {
              weChat: true,
              userId: JSON.parse(sessionStorage.getItem("userData")).userId,
              orgId: JSON.parse(sessionStorage.getItem("userData")).orgId,
            }
          }
        })
          .then(function (response) {
            // console.info(response.data.data)
            _this.pointsList = response.data.data.pointList;
            _this.count = parseInt(response.data.data.count);
          })
          .catch(function (error) {
            // console.info(error)
          })
      },
      handleCurrentChange:function(){
        this.getMemberPoints();
      }
    }
  }
</script>

